package helpex;

public class DbData {
    public Long saveAmount;
    public Long useAmount;
    public Long useCancelAmount;

    public Long getSaveAmount() {
        return saveAmount;
    }

    public void setSaveAmount(Long saveAmount) {
        this.saveAmount = saveAmount;
    }

    public Long getUseAmount() {
        return useAmount;
    }

    public void setUseAmount(Long useAmount) {
        this.useAmount = useAmount;
    }

    public Long getUseCancelAmount() {
        return useCancelAmount;
    }

    public void setUseCancelAmount(Long useCancelAmount) {
        this.useCancelAmount = useCancelAmount;
    }
}
